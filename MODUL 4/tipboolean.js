// Contoh tipe data Boolean dalam JavaScript

let benar = true;        // Nilai boolean true
let salah = false;       // Nilai boolean false

// Contoh boolean dari hasil perbandingan
let a = 10;
let b = 5;

let hasil1 = a > b;      // true, karena 10 lebih besar dari 5
let hasil2 = a === b;    // false, karena 10 tidak sama dengan 5

// Contoh penggunaan dalam struktur logika
if (hasil1) {
    console.log("a lebih besar dari b");
} else {
    console.log("a tidak lebih besar dari b");
}

console.log("Nilai benar:", benar);     // Output: true
console.log("Nilai salah:", salah);     // Output: false
console.log("Apakah a > b?:", hasil1);  // Output: true
console.log("Apakah a === b?:", hasil2);// Output: false
