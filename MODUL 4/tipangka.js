// Contoh tipe data angka dalam JavaScript

let angkaBulat = 42;        // Bilangan bulat
let angkaDesimal = 3.14;    // Bilangan desimal (float)
let hasilPerhitungan = angkaBulat + angkaDesimal; // Operasi penjumlahan

console.log("Angka Bulat:", angkaBulat);               // Output: 42
console.log("Angka Desimal:", angkaDesimal);           // Output: 3.14
console.log("Hasil Penjumlahan:", hasilPerhitungan);   // Output: 45.14
